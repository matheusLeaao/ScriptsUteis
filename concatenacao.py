# -*- coding:UTF-8 -*- #código de linguagem local, definir acentos nas palavras

#dedicado ao aprendizado de concatenação

a = "Matheus"
b = "Leão"

concatenação = a + " " + b 

#mostrar o resultado normalmente
print(concatenação)

#mostrar concatenação em range
print(concatenação[0])      #M
print(concatenação[0:1])    #MA
print(concatenação[0:2])    #MAT
print(concatenação[0:3])    #MATH
print(concatenação[0:4])    #MATHE
print(concatenação[0:5])    #MATHEU
print(concatenação[0:6])    #MATHEUS
print(concatenação[0:7])    #MATHEUS_
print(concatenação[0:8])    #MATHEUS_
print(concatenação[0:9])    #MATHEUS L
print(concatenação[0:10])   #MATHEUS LE
print(concatenação[0:11])   #MATHEUS LEÃ
print(concatenação[0:12])   #MATHEUS lEÃO